# Normalize citations (remove commas, et al., &, etc.)
def normalize_citation(citation):
    # Remove et al., &, commas, and extra spaces
    normalized = citation.replace(" et al.", "").replace(" & ", " ")
    normalized = normalized.replace(",", "").strip()
    return normalized.lower()

# Create normalized set for fast lookup
cited_normalized = {normalize_citation(c) for c in citations_in_text}

# Print all unique citations for verification
print(f"Total unique citations found in text: {len(citations_in_text)}")
print(f"Total normalized citations: {len(cited_normalized)}")
print("\nNormalized citations:")
for c in sorted(cited_normalized):
    print(f"  - {c}")
